weather = 'snowy'

case weather
when 'sunny'
    puts "It's a really nice day, no jacket necesary!"
when 'rainy'
    puts "Grab an umbrella and those rain boots!"
when 'cloudy'
    puts "Maybe grab a light sweater"
when 'snowy'
    puts "Wear your winter coat...or stay indoors"
when 'windy'
    puts "A light jacket may be needed"
else
    puts "The weather can be unpredictable, always be prepared!"
end